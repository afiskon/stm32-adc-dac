void UART_Printf(const char* fmt, ...) {
    char buff[256];
    va_list args;
    va_start(args, fmt);
    vsnprintf(buff, sizeof(buff), fmt, args);
    HAL_UART_Transmit(&huart1, (uint8_t*)buff, strlen(buff),
                      HAL_MAX_DELAY);
    va_end(args);
}

void init() {
    ST7735_Init();
    ST7735_FillScreen(ST7735_BLACK);

    HAL_ADC_Start(&hadc1);
    HAL_DAC_Start(&hdac, DAC_CHANNEL_1);
    UART_Printf("Ready!\r\n");
    
    HAL_Delay(1);
}

void loop() {
    HAL_Delay(100);

    HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
    uint32_t adc_val = HAL_ADC_GetValue(&hadc1);

    HAL_DAC_SetValue(&hdac, DAC_CHANNEL_1, DAC_ALIGN_12B_R, adc_val);

    char tmp[128];
    snprintf(tmp, sizeof(tmp), "ADC: %04lu", adc_val); // 0 .. 4095
    ST7735_WriteString(11*0, 18*0, tmp, Font_11x18, ST7735_WHITE, ST7735_BLACK);
}

